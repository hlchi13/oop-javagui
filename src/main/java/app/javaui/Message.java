package app.javaui;

public class Message {
    public String message;
    public String side;

    public void setMessage(String message) {
        this.message = message;
    }

    public void setSide(String side) {
        this.side = side;
    }

    public String getMessage() {
        return message;
    }

    public String getSide() {
        return side;
    }
}
